import java.util.Scanner;
class Sum
  {
   public static void main(String args[])
        {
         int num1,num2,sum;
         Scanner s=new Scanner(System.in);
         System.out.println("Enter the two numbers:");
         num1=s.nextInt();
         num2=s.nextInt();
         sum=num1+num2;
         System.out.println("Sum is:"+sum);
        }
  }       
         

